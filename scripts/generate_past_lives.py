def era_year(era: str) -> int:
    """시대 문자열을 정렬용 정수(연도)로 변환. 기원전은 음수, '만' 단위 처리."""
    bc = "기원전" in era
    m_man = re.search(r"(\d+(?:\.\d+)?)\s*만", era)
    if m_man:
        val = int(float(m_man.group(1)) * 10000)
        return -val if bc else val
    m = re.search(r"(\d+)", era.replace(",", ""))
    if m:
        val = int(m.group(1))
        return -val if bc else val
    return 10 ** 9  # 알 수 없으면 맨 뒤로


def select_evenly(pool, n):
    """pool에서 n개를 시대 분포가 고르도록 균등 간격으로 뽑는다."""
    if n >= len(pool):
        return list(pool)
    idxs = sorted({round(k * len(pool) / n) for k in range(n)})
    i = 0
    while len(idxs) < n and i < len(pool):
        if i not in idxs:
            idxs.append(i)
        i += 1
    idxs = sorted(idxs)[:n]
    return [pool[i] for i in idxs]


def main() -> None:
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

    # 1) 생물/존재와 사람을 분리한다.
    creatures = [row for row in DATA if row[0] in CREATURE_NAMES] + list(EXTRA_CREATURES)
    humans_pool = [row for row in DATA if row[0] not in CREATURE_NAMES]

    # 2) 생물은 전부 담고, 사람은 시대가 고루 퍼지도록 나머지 수만큼 뽑아 총 150건을 맞춘다.
    TARGET = 150
    humans_needed = TARGET - len(creatures)
    humans = select_evenly(humans_pool, humans_needed)

    # 3) 분류 태그를 붙이고 시대순으로 정렬한다.
    tagged = [(*row, "생물") for row in creatures] + [(*row, "사람") for row in humans]
    tagged.sort(key=lambda t: era_year(t[1]))

    records = []
    for i, (occ, era, cod, ach, mem, kind) in enumerate(tagged, start=1):
        records.append({
            "id": i,
            "kind": kind,
            "occupation": occ,
            "era": era,
            "causeOfDeath": cod,
            "achievement": ach,
            "memory": mem,
        })

    assert len(records) == TARGET, f"150건이어야 하는데 {len(records)}건입니다."
    n_creature = sum(1 for r in records if r["kind"] == "생물")
    print(f"분류: 생물 {n_creature}건 / 사람 {len(records) - n_creature}건")

    # 1) app/data/pastLives.json (앱 탑재용)
    json_dir = os.path.join(root, "app", "data")
    os.makedirs(json_dir, exist_ok=True)
    json_path = os.path.join(json_dir, "pastLives.json")
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(records, f, ensure_ascii=False, indent=2)

    # 2) data/past_lives.xlsx (엑셀 원본)
    from openpyxl import Workbook
    from openpyxl.styles import Font, Alignment, PatternFill

    data_dir = os.path.join(root, "data")
    os.makedirs(data_dir, exist_ok=True)
    xlsx_path = os.path.join(data_dir, "past_lives.xlsx")

    wb = Workbook()
    ws = wb.active
    ws.title = "전생 데이터"

    headers = ["번호", "분류", "전생의 직업 또는 존재", "시대", "사인(죽은 이유)",
               "전생의 업적", "사람들의 기억"]
    ws.append(headers)

    header_fill = PatternFill("solid", fgColor="4B2E83")
    header_font = Font(bold=True, color="FFFFFF")
    for cell in ws[1]:
        cell.fill = header_fill
        cell.font = header_font
        cell.alignment = Alignment(horizontal="center", vertical="center")

    for r in records:
        ws.append([r["id"], r["kind"], r["occupation"], r["era"], r["causeOfDeath"],
                   r["achievement"], r["memory"]])

    widths = [6, 8, 28, 18, 34, 44, 40]
    for idx, w in enumerate(widths, start=1):
        ws.column_dimensions[chr(64 + idx)].width = w
    for row in ws.iter_rows(min_row=2):
        for cell in row:
            cell.alignment = Alignment(vertical="center", wrap_text=True)
    ws.freeze_panes = "A2"

    wb.save(xlsx_path)

    print(f"OK: {len(records)}건 생성")
    print(f"  - {json_path}")
    print(f"  - {xlsx_path}")


if __name__ == "__main__":
    main()
